"""意图分类器 — 判断问题是否需要知识库检索"""

# 需要知识库的关键词（金融、政策、法规等领域）
KNOWLEDGE_KEYWORDS = [
    # 金融
    "金融", "投资", "融资", "股票", "债券", "基金", "期货", "外汇", "利率", "汇率",
    "货币", "银行", "贷款", "存款", "保险", "信托", "理财", "资本市场",
    "风险管理", "金融监管", "衍生品", "证券", "股息", "控股",
    # 政策法规
    "政策", "法规", "法律", "条例", "规定", "办法", "通知", "意见", "公告",
    "国务院", "证监会", "银保监", "人民银行", "财政部", "发改委",
    "监管", "合规", "审批", "备案", "行政许可",
    # 经济
    "经济", "GDP", "通货膨胀", "通货紧缩", "财政", "税收", "预算",
    "货币政策", "存款准备金", "绿色金融", "普惠金融", "金融科技",
    "数字货币", "数字经济", "市场准入", "反垄断",
    # 市场
    "市场", "交易", "价格", "供需", "竞争",
]

# 不需要知识库的通用对话（直接LLM回答）
GENERAL_PATTERNS = [
    "你好", "谢谢", "再见", "帮助", "你会什么",
    "介绍一下你自己", "你是谁", "聊天", "天气", "时间",
]


def classify(question: str) -> str:
    """
    返回:
      "rag"     — 需要知识库检索
      "general" — 通用问答，不需要知识库
    """
    q = question.strip()

    # 先检查是否包含专业知识关键词 → 走 RAG
    for kw in KNOWLEDGE_KEYWORDS:
        if kw in q:
            return "rag"

    # 检查是否是通用对话 → 走 general
    for pattern in GENERAL_PATTERNS:
        if pattern in q:
            return "general"

    # 默认走 RAG（专业知识平台）
    return "rag"
